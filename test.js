// 現在の日時を表示
console.log('現在の日時:', new Date().toLocaleString());

// 簡単な計算
const num1 = 10;
const num2 = 5;
console.log(`${num1} + ${num2} = ${num1 + num2}`);
console.log(`${num1} - ${num2} = ${num1 - num2}`);
console.log(`${num1} * ${num2} = ${num1 * num2}`);
console.log(`${num1} / ${num2} = ${num1 / num2}`);

